@implementation ZCNavigationController

- (void)viewDidLoad {
    [super viewDidLoad];
    self.navigationBar.barTintColor = [UIColor blackColor];
    self.interactivePopGestureRecognizer.delegate = nil;
    
    // Do any additional setup after loading the view.
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}
+(void)initialize{

    UINavigationBar *bar = [UINavigationBar appearance];
    [bar setBackgroundImage:[UIImage imageNamed:@"navigationbarBackgroundWhite"] forBarMetrics:UIBarMetricsDefault];
      UIBarButtonItem *item = [UIBarButtonItem appearance];
    [bar setTitleTextAttributes:@{NSFontAttributeName:[UIFont  boldSystemFontOfSize:20]}];
    NSMutableDictionary *itemAttris = [NSMutableDictionary dictionary];
    itemAttris[NSForegroundColorAttributeName] = [UIColor blackColor];
    itemAttris[NSFontAttributeName] = [UIFont systemFontOfSize:17];
    NSMutableDictionary *itemDisabledAttrs = [NSMutableDictionary dictionary];
    itemDisabledAttrs[NSForegroundColorAttributeName] = [UIColor lightGrayColor];
    [item setTitleTextAttributes:itemDisabledAttrs forState:UIControlStateDisabled];

}
